// Функция для сортировки массива методом сортировки вставкой
function insertionSort(arr) {
  const n = arr.length; // Получаем длину массива
  // Начинаем с i = 1, так как элемент с индексом 0 считается отсортированным
  for (let i = 1; i < n; i++) {
    const current = arr[i]; // Запоминаем текущий элемент
    let j = i - 1; // Инициализируем индекс предыдущего элемента
    // Начинаем сравнение текущего элемента с предыдущими элементами и двигаемся к началу массива
    // Пока не достигли начала массива и пока предыдущий элемент больше текущего
    while (j >= 0 && arr[j] > current) {
      arr[j + 1] = arr[j]; // Перемещаем предыдущий элемент на следующую позицию
      j--; // Уменьшаем индекс для перехода к следующему предыдущему элементу
    }
    arr[j + 1] = current; // Вставляем текущий элемент на свое место
  }
  return arr; // Возвращаем отсортированный массив
}

// Пример использования:
const array = [5, 2, 4, 6, 1, 3];
console.log("Before sorting:", array);
const sortedArray = insertionSort(array);
console.log("After sorting:", sortedArray);
